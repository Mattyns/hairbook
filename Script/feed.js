function curtir1() {
    
    var curtidas = document.getElementById('numcurtidas')
    var fundolike = document.getElementById('fundolike')
    var like = window.document.getElementById('like')
    like.style.color = ('white')
    fundolike.style.backgroundColor = ('red')
    curtidas.innerText = '51 Curtidas'
    like.setAttribute("onclick","descurtir1(this);");
    
}

function descurtir1() {
    var curtidas = document.getElementById('numcurtidas')
    var fundolike = document.getElementById('fundolike')
    var like = window.document.getElementById('like')
    like.style.color = ('black')
    fundolike.style.backgroundColor = ('rgba(128, 128, 128, 0.259)')
    curtidas.innerText = '50 Curtidas'
    like.setAttribute("onclick","curtir1(this);");
    
    


}




